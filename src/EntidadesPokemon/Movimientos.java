
package EntidadesPokemon;

/**
 *
 * @author Manuel
 */
public class Movimientos {
    String nombre;
    String tipo;
    int precision;
    int pp;
    int ppiniciales;

    public Movimientos() {
    }

    public Movimientos(String nombre, String tipo, int precision, int pp, int ppiniciales) {
        this.nombre = nombre;
        this.tipo = tipo;
        this.precision = precision;
        this.pp = pp;
        this.pp = ppiniciales;
    }    
}
